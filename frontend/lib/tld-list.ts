// Helper function to check if a TLD is supported
export const isTLDSupported = (tld: string): boolean => {
  const cleanTLD = tld.toLowerCase().replace('.', '')
  return SUPPORTED_TLD_LIST.gTLDs.includes(cleanTLD) || 
         SUPPORTED_TLD_LIST.ccTLDs.includes(cleanTLD)
}

// Get all supported TLDs as a flat array
export const getAllSupportedTLDs = (): string[] => {
  return [...SUPPORTED_TLD_LIST.gTLDs, ...SUPPORTED_TLD_LIST.ccTLDs]
}

// Get TLDs by category
export const getTLDsByCategory = (category: 'gTLD' | 'ccTLD'): string[] => {
  if (category === 'gTLD') return SUPPORTED_TLD_LIST.gTLDs
  if (category === 'ccTLD') return SUPPORTED_TLD_LIST.ccTLDs
  return []
}
